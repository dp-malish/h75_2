На проведені роботи по реанімації працездатності планшета надається гарантія, термін якої коливається в залежності від різновиду ремонту, моделі планшета та його віку. Ми надаємо гарантійні зобов'язання тільки на виконані нами роботи в сервісному центрі або на нові деталі, які були замінені при ремонті.
При виникненні гарантійного випадку - сервісний центр безкоштовно виправить поломку або ж замінить на новий вийшов з ладу елемент, який знаходився на гарантії.





